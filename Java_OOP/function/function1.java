package Java_OOP.function;

public class function1 {
    public static void display(){
        System.out.println("I love my India");
        System.out.println("I live in India");
        System.out.println("I am Happy");
    }

    public static void main(String[] args) {
        System.out.println("hello");
        display();
        System.out.println("Hi");
        display();
        System.out.println("Bye");
        display();
    }
    
}
// the function is a block of code that contains the single or multiple statements and does a specific task. In java function is also known as method 

