package basic.ConditionalThinking;

public class larger {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 23;
        if(num1 > num2){
            System.out.println("num1 is larger");
        }
        else{
            System.out.println("num2 is larger");
        }

    }
}
