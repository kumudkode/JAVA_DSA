package basic.ConditionalThinking;
 public class divisibleby5{
    public static void main(String[] args) {
        int num = 20;
        if(num %5 ==0){
            System.out.println("Number is divisible by 5");
        }
        else{
            System.out.println("Number is not divisible by 5");
        }
    }
 }
