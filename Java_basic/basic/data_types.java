package basic;

public class data_types {
    public static void main(String[] args) {
        byte a= 127; // range from-128 to 127
        System.out.println(a);
        char b= '2'; // only as a 1 character will allowed 
        System.out.println(b);
        boolean c=true; // storing true or false
        boolean d=false;
        System.out.println(c+" "+d);
        float e= 10.3f;
        System.out.println(e);
        double f=10.6;
        



    }
}
